class ColoredFormatter(logging.Formatter):
    """Ğ¦Ğ²ĞµÑ‚Ğ½Ğ¾Ğ¹ Ñ„Ğ¾Ñ€Ğ¼Ğ°Ñ‚Ñ‚ĞµÑ€ Ğ´Ğ»Ñ Ğ»Ğ¾Ğ³Ğ¾Ğ² Ñ Ğ¼Ğ¸Ğ½Ğ¸Ğ¼Ğ°Ğ»Ğ¸ÑÑ‚Ğ¸Ñ‡Ğ½Ñ‹Ğ¼ Ğ¾Ñ„Ğ¾Ñ€Ğ¼Ğ»ĞµĞ½Ğ¸ĞµĞ¼"""
    
    # ANSI ĞºĞ¾Ğ´Ñ‹ Ñ†Ğ²ĞµÑ‚Ğ¾Ğ²
    COLORS = {
        'DEBUG': '\033[36m',     # Cyan
        'INFO': '\033[32m',      # Green
        'WARNING': '\033[33m',   # Yellow
        'ERROR': '\033[31m',     # Red
        'CRITICAL': '\033[35m',  # Magenta
    }
    RESET = '\033[0m'
    BOLD = '\033[1m'
    
    # Ğ¡Ğ¾ĞºÑ€Ğ°Ñ‰ĞµĞ½Ğ¸Ñ ÑƒÑ€Ğ¾Ğ²Ğ½ĞµĞ¹
    LEVEL_ABBR = {
        'DEBUG': 'D',
        'INFO': 'I',
        'WARNING': 'W',
        'ERROR': 'E',
        'CRITICAL': 'C'
    }
    
    def format(self, record):
        # Ğ¦Ğ²ĞµÑ‚ Ğ´Ğ»Ñ ÑƒÑ€Ğ¾Ğ²Ğ½Ñ
        levelname_color = self.COLORS.get(record.levelname, self.RESET)
        
        # Ğ¤Ğ¾Ñ€Ğ¼Ğ°Ñ‚Ğ¸Ñ€ÑƒĞµĞ¼ Ğ²Ñ€ĞµĞ¼Ñ
        time_str = self.formatTime(record, '%H:%M:%S')
        
        # Ğ¡Ğ¾ĞºÑ€Ğ°Ñ‰Ñ‘Ğ½Ğ½Ñ‹Ğ¹ ÑƒÑ€Ğ¾Ğ²ĞµĞ½ÑŒ
        level_abbr = self.LEVEL_ABBR.get(record.levelname, '?')
        level_str = f"{levelname_color}{self.BOLD}[{level_abbr}]{self.RESET}"
        
        # Ğ¡Ğ¾Ğ¾Ğ±Ñ‰ĞµĞ½Ğ¸Ğµ
        message = record.getMessage()
        
        return f"{time_str} {level_str} {message}"


